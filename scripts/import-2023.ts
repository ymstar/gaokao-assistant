function buildData(
  entries: [number, number, number][],
  year: number,
  group: '物理类' | '历史类'
) {
  const sorted = entries.sort((a, b) => b[0] - a[0]);
  return {
    year,
    group,
    maxScore: sorted[0][0],
    minScore: sorted[sorted.length - 1][0],
    totalCandidates: sorted[sorted.length - 1][2],
    entries: sorted.map(([score, count, cumulative]) => ({ score, count, cumulative })),
    meta: {
      source: '河北省教育考试院',
      sourceUrl: 'https://www.hebeea.edu.cn/c/2023-06-24/488564.html',
      publishedAt: '2023-06-24',
      quality: 'official' as const,
      importedAt: new Date().toISOString(),
    },
  };
}

